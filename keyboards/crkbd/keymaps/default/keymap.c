static uint32_t idle_timer = 0;  // Variable para almacenar el tiempo de inactividad
static bool is_idle = false;

void matrix_scan_user(void) {
    if (timer_elapsed32(idle_timer) > IDLE_TIMER) {  // IDLE_TIMER ms = 5 segundos
        // Si ha pasado m√°s de 5 segundos de inactividad, reinicia el time
        is_idle = true;
    }else{
        is_idle = false;
    }
}

bool oled_task_user(void) {
    if (timer_elapsed32(idle_timer) > IDLE_TIMER) {  // Si ha pasado m√°s de 5 segundos de inactividad
        oled_clear();  // Limpia la pantalla
        oled_write_raw_P(logo, sizeof(logo));  // Muestra el logo
    } else {
        // Muestra el contenido normal (WPM y flecha)
        oled_render_arrow();
        oled_render_wpm_bar();
        oled_render_cute_cat();
    }
    if( !is_keyboard_master()){
        oled_clear();  // Limpia la pantalla
        oled_write_raw_P(logo_slave, sizeof(logo_slave));  // Muestra el logo
    }
    return false;
}
