static uint8_t current_width = 0;
static uint16_t shrink_accumulator = 0;
#define ACCELERATION_RATE 16
#define FIXED_POINT_SHIFT 8
#define MIN_VELOCITY 32
#define WPM_UPDATE_INTERVAL 50

void oled_write_vertical_block(uint8_t x_start, uint8_t y_start, uint8_t width, uint8_t height, bool on) {
    for (uint8_t x = x_start; x < x_start + width; x++) {
        for (uint8_t y = y_start; y < y_start + height; y++) {
            oled_write_pixel(x, y, on);
        }
    }
}

void oled_render_wpm_bar(void) {
    static uint32_t last_update = 0;
    uint32_t now = timer_read32();
    if (TIMER_DIFF_32(now, last_update) < WPM_UPDATE_INTERVAL) {
        return;
    }
    last_update = now;

    uint16_t wpm = get_current_wpm();
    uint16_t max_wpm = 245;
    uint16_t target_width = (wpm * 127) / max_wpm; // Scaled to 127 max
if (target_width >= current_width) {
        current_width = target_width;
        shrink_accumulator = 0;
    } else {
        if (current_width > target_width || shrink_accumulator >= MIN_VELOCITY) {
            shrink_accumulator += ACCELERATION_RATE;
            uint8_t shrink_amount = shrink_accumulator >> FIXED_POINT_SHIFT;

            if (shrink_amount > 0) {
                current_width = (current_width > shrink_amount)
                               ? current_width - shrink_amount
                               : 0;
            }
        } else if (current_width > target_width) {
            current_width--;
        }
    }

    uint8_t bar_width = 8;
    uint8_t start_x = 24;

    // Draw active portion (bottom-up)
    if (current_width > 0) {
        oled_write_vertical_block(start_x, 127 - current_width, bar_width, current_width + 1, true);
    }

    // Clear inactive portion (top section)
    if (current_width < 127) {
        oled_write_vertical_block(start_x, 0, bar_width, 127 - current_width, false);
    }
}

